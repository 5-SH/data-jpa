    @Test
    public void paging() {
        Team team = new Team("teamA");
        teamRepository.save(team);

        memberRepository.save(new Member("member1", 10, team));
        memberRepository.save(new Member("member2", 10, team));
        memberRepository.save(new Member("member3", 10, team));
        memberRepository.save(new Member("member4", 10, team));
        memberRepository.save(new Member("member5", 10, team));

        int age = 10;
        PageRequest pageRequest = PageRequest.of(0, 3, Sort.by(Sort.Direction.DESC, "username"));

        Page<Member> page = memberRepository.findByAge(age, pageRequest);
//        Slice<Member> page = memberRepository.findByAge(age, pageRequest);
//        List<Member> page = memberRepository.findByAge(age, pageRequest);

        Page<MemberDto> toMap = page.map(member -> new MemberDto(member.getId(), member.getUsername(), member.getTeam().getName()));
        for (MemberDto memberDto : toMap.getContent()) {
            System.out.println("memberDto = " + memberDto);
        }

        List<Member> content = page.getContent();
        long totalElements = page.getTotalElements();
        for (Member member : content) {
            System.out.println("member = " + member);
        }
        System.out.println("totalElements = " + totalElements);
        System.out.println("size : " + content.size());
        System.out.println("getTotalElements : " + page.getTotalElements());
        System.out.println("getNumber : " + page.getNumber());
        System.out.println("getTotalPages : " + page.getTotalPages());
        System.out.println("isFirst : " + page.isFirst());
        System.out.println("hasNext : " + page.hasNext());

        assertThat(content.size()).isEqualTo(3);
        assertThat(page.getTotalElements()).isEqualTo(5);
        assertThat(page.getNumber()).isEqualTo(0);
        assertThat(page.getTotalPages()).isEqualTo(2);
        assertThat(page.isFirst()).isTrue();
        assertThat(page.hasNext()).isTrue();
    }

